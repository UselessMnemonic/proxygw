package main

import (
	"bytes"
	"errors"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"strings"

	"gopkg.in/yaml.v3"
)

type pluginDecl struct {
	source string
	name   string
}

func main() {
	if err := run(); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func run() error {
	root, module, err := repoRoot()
	if err != nil {
		return err
	}

	configPath := filepath.Join(root, "plugin.yaml")
	decls, err := parsePluginYaml(configPath)
	if err != nil {
		return err
	}

	decls, err = resolvePlugins(root, module, decls)
	if err != nil {
		return err
	}

	outputPath := filepath.Join(root, "cmd", "proxygw", "plugin.go")
	return writePluginFile(outputPath, decls)
}

func repoRoot() (string, string, error) {
	wd, err := os.Getwd()
	if err != nil {
		return "", "", fmt.Errorf("determine working directory: %w", err)
	}

	root := filepath.Clean(filepath.Join(wd, "..", ".."))
	modPath := filepath.Join(root, "go.mod")
	if _, err = os.Stat(modPath); err != nil {
		return "", "", fmt.Errorf("determine repo root from %s: %w", wd, err)
	}

	modData, err := os.ReadFile(modPath)
	if err != nil {
		return "", "", fmt.Errorf("read %s: %w", modPath, err)
	}

	module := ""
	for _, line := range strings.Split(string(modData), "\n") {
		trimmed := strings.TrimSpace(strings.TrimRight(line, "\r"))
		if !strings.HasPrefix(trimmed, "module ") {
			continue
		}

		module = strings.TrimSpace(strings.TrimPrefix(trimmed, "module "))
		break
	}
	if module == "" {
		return "", "", fmt.Errorf("%s: module directive not found", modPath)
	}

	return root, module, nil
}

func parsePluginYaml(path string) ([]pluginDecl, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, fmt.Errorf("read %s: %w", path, err)
	}

	var doc yaml.Node
	if err = yaml.Unmarshal(data, &doc); err != nil {
		return nil, fmt.Errorf("parse %s: %w", path, err)
	}
	if len(doc.Content) == 0 {
		return nil, fmt.Errorf("%s: plugins mapping is empty", path)
	}

	rootNode := &doc
	if rootNode.Kind == yaml.DocumentNode {
		rootNode = rootNode.Content[0]
	}
	if rootNode.Kind != yaml.MappingNode {
		return nil, fmt.Errorf("%s: root must be a mapping", path)
	}

	var pluginsNode *yaml.Node
	for i := 0; i < len(rootNode.Content); i += 2 {
		keyNode := rootNode.Content[i]
		valueNode := rootNode.Content[i+1]
		if keyNode.Kind == yaml.ScalarNode && keyNode.Value == "plugins" {
			pluginsNode = valueNode
			break
		}
	}
	if pluginsNode == nil {
		return nil, fmt.Errorf("%s: plugins mapping is empty", path)
	}
	if pluginsNode.Kind == yaml.DocumentNode {
		if len(pluginsNode.Content) == 0 {
			return nil, fmt.Errorf("%s: plugins mapping is empty", path)
		}
		pluginsNode = pluginsNode.Content[0]
	}
	if pluginsNode.Kind != yaml.MappingNode {
		return nil, fmt.Errorf("%s: plugins must be a mapping", path)
	}

	decls := make([]pluginDecl, 0, len(pluginsNode.Content)/2)
	for i := 0; i < len(pluginsNode.Content); i += 2 {
		sourceNode := pluginsNode.Content[i]
		nameNode := pluginsNode.Content[i+1]
		if sourceNode.Kind != yaml.ScalarNode {
			return nil, fmt.Errorf("%s: plugin source at entry %d must be a scalar", path, (i/2)+1)
		}
		if nameNode.Kind != yaml.ScalarNode {
			return nil, fmt.Errorf("%s: plugin name for source %q must be a scalar", path, sourceNode.Value)
		}

		decls = append(decls, pluginDecl{
			source: sourceNode.Value,
			name:   nameNode.Value,
		})
	}

	return decls, nil
}

func resolvePlugins(root string, module string, pluginDecls []pluginDecl) ([]pluginDecl, error) {
	resolved := make([]pluginDecl, 0, len(pluginDecls))
	for _, decl := range pluginDecls {
		if decl.source == "" {
			return nil, errors.New("source must not be empty")
		}
		if decl.name == "" {
			return nil, fmt.Errorf("name must not be empty")
		}

		localPath := filepath.Join(root, "plugin", filepath.FromSlash(decl.source))
		if info, err := os.Stat(localPath); err == nil && info.IsDir() {
			pkg := filepath.ToSlash(filepath.Join(module, "plugin", filepath.FromSlash(decl.source)))
			resolved = append(resolved, pluginDecl{pkg, decl.name})
		} else {
			resolved = append(resolved, decl)
		}
	}
	return resolved, nil
}

func writePluginFile(path string, decls []pluginDecl) error {
	var buf bytes.Buffer
	buf.WriteString("package main\n\n")
	buf.WriteString("// Code generated by go generate; DO NOT EDIT.\n\n")
	buf.WriteString("import (\n")
	for _, decl := range decls {
		buf.WriteString(fmt.Sprintf("\t_ %q\n", decl.source))
	}
	buf.WriteString(")\n")
	buf.WriteString("\n")
	buf.WriteString("func init() {\n")
	buf.WriteString("\tpluginDecls = []pluginDecl{\n")
	for _, decl := range decls {
		buf.WriteString(fmt.Sprintf("\t\t{%q, %q},\n", decl.source, decl.name))
	}
	buf.WriteString("\t}\n")
	buf.WriteString("}\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("format generated file: %w", err)
	}

	if err = os.WriteFile(path, formatted, 0o644); err != nil {
		return fmt.Errorf("write %s: %w", path, err)
	}
	return nil
}
